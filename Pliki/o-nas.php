                <strong>Międzynarodowe Stowarzyszenie Ciągarskie w Polsce – The Wire Association International, Poland Chapter</strong> jest polskim oddziałem amerykańskiego stowarzyszenia The Wire Association International – pierwszym na świecie oddziałem WAI poza granicami USA.
            </p>
            <p>
                Organizacja The Wire Association International (WAI), Inc. została założona w roku 1930. Jest to międzynarodowe stowarzyszenie specjalistów działające na rzecz przemysłu ciągarskiego, prowadzone przez wolontariuszy z całego świata. Kwatera główna organizacji znajduje się w Stanach Zjednoczonych, w Guilford w stanie Connecticut. Organizacja zbiera i rozpowszechnia wszelkie informacje i nowiny techniczne, produkcyjne i biznesowe odnośnie branży kablowej i druciarskiej. W tym celu WAI od lat organizuje i sponsoruje różnorodne wystawy: Interwire i Wire Expo, międzynarodowe konferencje, warsztaty edukacyjne, kursy oraz programy rozwoju karier. WAI publikuje prestiżowy miesięcznik Wire Journal International, The WAI Connection, WJI Reference Guide oraz różnorodne książki i raporty. W 70 krajach na całym świecie WAI ma swoich reprezentantów.
            </p>
            <p>
                WAI wspiera lokalne oddziały organizacji. Ich działania dają dodatkowe spojrzenie na sprawy zarówno lokalne, jak i globalne. Istnienie owej lokalnej sieci daje możliwość uczestnictwa w spotkaniach, prezentacjach, działaniach i spotkaniach towarzyskich. Jej początek miał miejsce w Nowej Anglii w 1994 roku, gdzie WAI utworzyła 6 oddziałów w stanach Zjednoczonych i 3 poza ich granicami, czyli w Polsce, we Włoszech i w Indiach. WAI prowadzona przez wolontariuszy z całego świata daje możliwość kształtowania przyszłości przemysłu ciągarskiego.
            </p>
            <p>
