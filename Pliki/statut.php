            <h3 class="text-lg font-bold text-center mt-6 pt-4" id="rozdzial-1">
                Rozdział I<br/>POSTANOWIENIA OGÓLNE
            </h3>
            <p class="font-bold text-center">§ 1</p>
            <p>1. Stowarzyszenie nosi nazwę: „Międzynarodowe Stowarzyszenie Ciągarskie w Polsce” - zwane dalej Stowarzyszeniem.</p>
            <p>W stosunkach międzynarodowych Stowarzyszenie może używać nazwy: The Wire Association International – Poland Chapter.</p>
            <p>2. Siedziba Stowarzyszenia i jego organów znajduje się w Częstochowie, al. Armii Krajowej 23/25.</p>
            
            <p class="font-bold text-center">§ 2</p>
            <p>Stowarzyszenie posiada osobowość prawną i działa na podstawie przepisów ustawy z dnia 7 kwietnia 1989 r. - Prawo o stowarzyszeniach, ustawy z dnia 24 kwietnia 2003 r. o działalności pożytku publicznego i wolontariacie oraz niniejszego statutu.</p>
            
            <p class="font-bold text-center">§ 3</p>
            <p>1. Terenem działania Stowarzyszenia jest obszar Rzeczypospolitej Polskiej.</p>
            <p>2. Stowarzyszenie może być członkiem krajowych i międzynarodowych organizacji o podobnym profilu działania.</p>
            <p>3. Stowarzyszenie może prowadzić działalność poza granicami Rzeczypospolitej Polskiej zgodnie z obowiązującymi przepisami prawa.</p>
            <p>4. Stowarzyszenie ma prawo tworzyć jednostki terenowe w postaci oddziałów, które nie posiadają osobowości prawnej.</p>
            <p>5. Stowarzyszenie nie prowadzi działalności gospodarczej.</p>
            <p>6. Działalność Stowarzyszenia opiera się na społecznej pracy jego członków. Stowarzyszenie może zatrudniać pracowników, w tym swoich członków lub zlecać określone zadania innym podmiotom.</p>
            
            <p class="font-bold text-center">§ 4</p>
            <p>Stowarzyszenie może używać odznak i pieczęci zgodnie z właściwymi przepisami.</p>

            <h3 class="text-lg font-bold text-center mt-8 pt-4" id="rozdzial-2">
                Rozdział II<br/>CELE STOWARZYSZENIA I SPOSOBY DZIAŁANIA
            </h3>
            <p class="font-bold text-center">§ 5</p>
            <p>Celem Stowarzyszenia jest działanie na rzecz rozwoju polskiego przemysłu wyrobów metalowych, ciągarskiego, kablowego i innych gałęzi przemysłu z nimi związanych oraz jego promocji, przy jednoczesnej współpracy z czołowymi ośrodkami w tej dziedzinie na świecie.</p>
            
            <p class="font-bold text-center">§ 6</p>
            <p>I. Stowarzyszenie realizuje swoje cele poprzez:</p>
            <p>1. Organizowanie krajowych i międzynarodowych konferencji i sympozjów;</p>
            <p>2. Organizowanie dokształcania polskiej kadry technicznej w oparciu o kontakty z krajowymi i zagranicznymi ośrodkami naukowymi oraz organizacjami technicznymi;</p>
            <p>3. Umożliwianie korzystania ze stypendiów i innych środków pomocy;</p>
            <p>4. Umożliwienie dostępu do międzynarodowej prasy fachowej i opracowań tematycznych;</p>
            <p>5. Organizowanie kształcenia w zakresie fachowego języka obcego;</p>
            <p>6. Inicjowanie kontaktów gospodarczych w zakresie przemysłu ciągarskiego i wyrobów metalowych;</p>
            <p>7. Organizowanie działalności wydawniczej i szkoleniowej;</p>
            <p>8. Organizowanie targów i wystaw tematycznych;</p>
            <p>9. Pomoc w reklamie i promocji polskich produktów;</p>
            <p>10. Współpracę z krajowymi i zagranicznymi organizacjami branżowymi w zakresie wymiany myśli i doświadczeń;</p>
            <p>11. Nawiązywanie kontaktów i współpraca z jednostkami naukowymi, uczelniami wyższymi i zawodowymi szkołami średnimi z branży metalowej w specjalnościach ciągarstwo i otoczenie procesu ciągnienia oraz wyrobów metalowych;</p>
            <p>12. Pomoc w zakresie ochrony wzorów i patentów;</p>
            <p>13. Wyrażanie wniosków i opinii wobec instytucji i organów władzy;</p>
            <p>14. Opracowywanie opinii i ekspertyz fachowych z zakresu ciągarstwa i wyrobów metalowych na rzecz osób fizycznych, organizacji społecznych, podmiotów gospodarczych i różnego rodzaju organów;</p>
            <p>15. Występowania o projekty finansowane przez NCBiR, NCN oraz organizacje zagraniczne.</p>
            <p>II. Stowarzyszenie realizuje swoje cele poprzez odpłatną i nieodpłatną działalność statutową.</p>

            <h3 class="text-lg font-bold text-center mt-8 pt-4" id="rozdzial-3">
                Rozdział III<br/>CZŁONKOWIE STOWARZYSZENIA
            </h3>
            <p class="font-bold text-center">§ 7</p>
            <p>1. Członkowie Stowarzyszenia dzielą się na:</p>
            <p>a) członków zwyczajnych,</p>
            <p>b) członków wspierających,</p>
            <p>c) członków honorowych.</p>

            <p class="font-bold text-center">§ 8</p>
            <p>1. Członkami zwyczajnymi mogą być osoby fizyczne posiadające pełną zdolność do czynności prawnych i niepozbawione praw publicznych.</p>
            <p>2. Cudzoziemcy, bez względu na miejsce zamieszkania, mogą być członkami Stowarzyszenia zgodnie z przepisami obowiązującymi obywateli polskich.</p>
            <p>3. Osoby prawne mogą być członkami wspierającymi Stowarzyszenia.</p>
            <p>4. Członkami honorowymi mogą być osoby zasłużone dla nauki i przemysłu oraz inne osoby z tzw. otoczenia biznesowego.</p>

            <p class="font-bold text-center">§ 9</p>
            <p>1. Kandydat do Stowarzyszenia składa pisemną deklarację zawierającą oświadczenie o przystąpieniu i zobowiązanie do opłacania składek, a ponadto:</p>
            <p>a) osoby fizyczne: imię (imiona) i nazwisko, datę i miejsce urodzenia, adres,</p>
            <p>b) osoby prawne: nazwę (firmę), siedzibę, adres, numer Regon, dołączając także odpis z właściwego rejestru, jeżeli podlegają wpisowi do rejestru.</p>
            <p>2. O przyjęciu w poczet członków Stowarzyszenia decyduje Zarząd w formie uchwały. O swojej decyzji Zarząd niezwłocznie powiadamia pisemnie zainteresowanego.</p>
            <p>3. Od decyzji odmownej zainteresowanemu przysługuje prawo odwołania do Walnego Zebrania Członków, którego uchwała w tym przedmiocie jest ostateczna. Odwołanie należy wnieść w terminie 1 miesiąca od otrzymania pisemnej informacji o decyzji Zarządu.</p>

            <p class="font-bold text-center">§ 10</p>
            <p>1. Członek zwyczajny Stowarzyszenia ma w szczególności prawo do:</p>
            <p>a) uczestnictwa w Walnych Zebraniach Członków Stowarzyszenia i brania udziału w głosowaniu,</p>
            <p>b) wybierania i bycia wybranym do organów Stowarzyszenia,</p>
            <p>c) zgłaszania do organów Stowarzyszenia wniosków w sprawach związanych z jego działalnością i żądania informacji o sposobie ich załatwienia,</p>
            <p>d) uczestniczenia w spotkaniach i imprezach organizowanych przez Stowarzyszenie.</p>
            <p>2. Członek zwyczajny Stowarzyszenia jest zobowiązany:</p>
            <p>a) brać czynny udział w przedsięwzięciach organizowanych przez Stowarzyszenie,</p>
            <p>b) przestrzegać postanowień Statutu oraz uchwał organów Stowarzyszenia,</p>
            <p>c) nie naruszać solidarności organizacyjnej Stowarzyszenia,</p>
            <p>d) regularnie opłacać składki członkowskie.</p>

            <p class="font-bold text-center">§ 11</p>
            <p>1. Ustanie członkostwa w Stowarzyszeniu następuje wskutek:</p>
            <p>a) pisemnej rezygnacji z członkostwa,</p>
            <p>b) śmierci członka będącego osobą fizyczną, utraty przez niego pełnej zdolności do czynności prawnych albo utraty praw publicznych,</p>
            <p>c) likwidacji członka będącego osobą prawną,</p>
            <p>d) wykluczenia.</p>
            <p>2. Wykluczenie członka następuje w przypadku:</p>
            <p>a) nieusprawiedujenego zalegania z zapłatą składek przez okres dłuższy niż 6 miesięcy,</p>
            <p>b) postępowania rażąco sprzecznego z niniejszym Statutem,</p>
            <p>c) postępowania, które dyskwalifikuje daną osobę jako członka Stowarzyszenia lub godzi w jego dobre imię,</p>
            <p>d) działania na szkodę Stowarzyszenia.</p>
            <p>3. O wykluczeniu decyduje Zarząd w formie uchwały. O treści uchwały Zarząd powiadamia pisemnie zainteresowanego.</p>
            <p>4. Ustanie członkostwa z przyczyn określonych w ustępie 1 pkt a) - c) stwierdza Zarząd w formie uchwały, przepis ustępu 3 stosuje się odpowiednio.</p>
            <p>5. Od uchwał Zarządu, o których mowa w ustępie 3 i 4 przysługuje zainteresowanemu odwołanie do Walnego Zebrania Członków w terminie 1 miesiąca od pisemnego powiadomienia o treści uchwały. Uchwała Walnego Zebrania Członków jest ostateczna.</p>

            <p class="font-bold text-center">§ 12</p>
            <p>1. Członkiem wspierającym może być osoba fizyczna lub prawna bez względu na jej miejsce zamieszkania i siedzibę w kraju lub zagranicą, która za swoją zgodą zostanie przyjęta do Stowarzyszenia za okazaną pomoc w realizacji jego celów, po złożeniu deklaracji zawierającej dane, o których mowa w § 9 ust. 1 oraz oświadczenie o zakresie deklarowanej pomocy na rzecz Stowarzyszenia.</p>
            <p>2. Członek wspierający opłaca składkę członkowską w zadeklarowanej przez siebie wysokości.</p>
            <p>3. Skreślenie z listy członków wspierających następuje zgodnie z § 11.</p>

            <p class="font-bold text-center">§ 13</p>
            <p>1. Członkiem honorowym może być każda osoba fizyczna, bez względu na jej miejsce zamieszkania, której godność tę nada Walne Zebranie za szczególne zasługi dla Stowarzyszenia.</p>
            <p>2. Nadanie członkowi zwyczajnemu Stowarzyszenia godności członka honorowego, nie powoduje utraty przez niego praw i obowiązków jako członka zwyczajnego.</p>
            <p>3. Walne Zebranie może odebrać godność członka honorowego w wypadkach określonych w § 11 ust. 2 lit. b - d.</p>

            <p class="font-bold text-center">§ 14</p>
            <p>Członkowie wspierający i honorowi mają prawo uczestnictwa w pracach i imprezach organizowanych przez Stowarzyszenie, nie posiadają czynnego ani biernego prawa wyborczego. Uczestniczą w Walnym Zebraniu z głosem doradczym.</p>

            <h3 class="text-lg font-bold text-center mt-8 pt-4" id="rozdzial-4">
                Rozdział IV<br/>ORGANY STOWARZYSZENIA
            </h3>
            <p class="font-bold text-center">§ 15</p>
            <p>Organami Stowarzyszenia są:</p>
            <p>a) Walne Zebranie Członków,</p>
            <p>b) Zarząd Stowarzyszenia,</p>
            <p>c) Komisja Rewizyjna.</p>

            <p class="font-bold text-center">§ 16</p>
            <p>1. Wyboru Zarządu Stowarzyszenia i Komisji Rewizyjnej dokonuje Walne Zebranie Członków spośród członków Stowarzyszenia w głosowaniu tajnym. Walne Zebranie może zarządzić głosowanie jawne.</p>
            <p>2. Członkowie Zarządu i Komisji Rewizyjnej są wybierani na 4-letnią kadencję do czasu wyboru nowego Zarządu i Komisji Rewizyjnej. Mandat członków Zarządu i Komisji Rewizyjnej wygasa po pierwszym Walnym Zebraniu odbywającym się w roku kalendarzowym, w którym upływa okres kadencji.</p>
            <p>3. Mandat członka Zarządu lub Komisji Rewizyjnej wygasa przed upływem kadencji z powodu:</p>
            <p>a) ustania członkostwa w Stowarzyszeniu,</p>
            <p>b) pisemnej rezygnacji,</p>
            <p>c) odwołania przez Walne Zebranie Członków uchwałą podjętą większością 2/3 głosów.</p>
            <p>4. W przypadkach wygaśnięcia mandatu członka Zarządu lub Komisji Rewizyjnej przed upływem kadencji Walne Zebranie Członków dokonuje uzupełnienia składu na okres do upływu kadencji.</p>
            <p>5. Innym przypadkiem wygaśnięcia mandatu członka Zarządu lub Komisji Rewizyjnej w sytuacjach wskazanych w ust. 2 i ust. 3 lit. b pełni on swoją funkcję do czasu wyboru innej osoby na jego miejsce, chyba że Walne Zebranie Członków postanowi inaczej.</p>

            <p class="font-bold text-center">§ 17</p>
            <p>1. Wybór Zarządu i Komisji Rewizyjnej zapada zwykłą większością głosów w obecności co najmniej połowy ogólnej liczby członków organu, o ile Statut nie stanowi inaczej.</p>
            <p>2. Jeżeli w pierwszym terminie na Walnym Zebraniu Członków nie uczestniczy co najmniej połowa liczby członków, wówczas w drugim terminie posiedzenia uchwały mogą być podjęte odpowiednią większością głosów oddanych przez członków obecnych.</p>
            <p>3. Drugi termin powinien być podany w zawiadomieniu o posiedzeniu i nie może być wyznaczony wcześniej niż po upływie jednej godziny po pierwszym terminie.</p>

            <p class="font-bold text-center">§ 18</p>
            <p>1. Zwyczajne Walne Zebranie Członków zwoływane jest na podstawie uchwały Zarządu, przynajmniej raz na 4 lata, jako sprawozdawczo-wyborcze.</p>
            <p>2. Zarząd zawiadamia członków o terminie i proponowanym porządku obrad Walnego Zebrania Członków przynajmniej na miesiąc przed jego terminem.</p>
            <p>3. Zarząd jest zobowiązany zwołać Nadzwyczajne Walne Zebranie Członków na wniosek Komisji Rewizyjnej lub 1/3 członków Stowarzyszenia. Zarząd zwołuje Walne Zebranie niezwłocznie po otrzymaniu wniosku, termin odbycia Walnego Zebrania Członków musi przypadać w ciągu 2 miesięcy od chwili złożenia wniosku.</p>
            <p>4. Do żądania zwołania Nadzwyczajnego Walnego Zebrania Członków dołącza się proponowany porządek obrad, który Zarząd obowiązany jest uwzględnić. Porządek może być rozszerzony przez Zarząd o inne punkty.</p>

            <p class="font-bold text-center">§ 19</p>
            <p>1. Do kompetencji Walnego Zebrania Członków w szczególności należy:</p>
            <p>a) uchwalanie programów organizacyjnej i finansowej działalności Stowarzyszenia,</p>
            <p>b) rozpatrywanie i zatwierdzanie sprawozdania Komisji Rewizyjnej z działalności Stowarzyszenia,</p>
            <p>c) rozpatrywanie i zatwierdzanie sprawozdań Zarządu Stowarzyszenia z działalności minionej kadencji,</p>
            <p>d) udzielanie, po wysłuchaniu wniosków Komisji Rewizyjnej, absolutorium ustępującemu Zarządowi,</p>
            <p>e) wybór Prezesa i Członków Zarządu,</p>
            <p>f) wybór członków Komisji Rewizyjnej,</p>
            <p>g) uchwalenie Statutu Stowarzyszenia lub jego zmiany,</p>
            <p>h) nadanie członkostwa honorowego Stowarzyszenia,</p>
            <p>i) ustalanie wysokości składek członkowskich,</p>
            <p>j) ustalanie wysokości wynagrodzenia dla członków zarządu za czynności wykonywane w związku z pełnioną funkcją,</p>
            <p>k) podjęcie uchwały w sprawie rozwiązania Stowarzyszenia i przeznaczenia jego majątku,</p>
            <p>l) podejmowanie uchwał w sprawie przystąpienia do innych organizacji,</p>
            <p>m) powoływanie i likwidacja oddziałów Stowarzyszenia,</p>
            <p>n) podejmowanie uchwał w przypadkach określonych niniejszym Statutem oraz w innych sprawach niezastrzeżonych do kompetencji pozostałych organów.</p>
            <p>2. Walne Zebranie Członków pracuje według ustalonego porządku obrad. Proponowany porządek obrad może być przez Walne Zebranie Członków zmieniony lub rozszerzony. Porządek nie może być rozszerzony o punkty dotyczące zmiany Statutu lub rozwiązania Stowarzyszenia.</p>
            <p>3. Walne Zebranie Członków, po otwarciu go przez Prezesa Zarządu Stowarzyszenia, wybiera Przewodniczącego Zebrania, a na jego wniosek Wiceprzewodniczącego i Sekretarza, którzy dalej prowadzą obrady.</p>

            <p class="font-bold text-center">§ 20</p>
            <p>1. Zarząd składa się z 5 ÷ 7 członków, w tym Prezesa, Wiceprezesów, Skarbnika i Sekretarza.</p>
            <p>2. Prezes Zarządu kieruje pracami Zarządu. W razie jego nieobecności zastępuje go Wiceprezes lub wskazany członek Zarządu.</p>
            <p>3. Zarząd kieruje działalnością Stowarzyszenia i reprezentuje je na zewnątrz.</p>

            <p class="font-bold text-center">§ 21</p>
            <p>1. Do zakresu działania Zarządu Stowarzyszenia należy w szczególności:</p>
            <p>a) realizacja uchwał Walnego Zebrania Członków,</p>
            <p>b) kierowanie bieżącą pracą Stowarzyszenia i zarządzanie jego majątkiem,</p>
            <p>c) podejmowanie uchwał dotyczących przyjęcia członków i ustania członkostwa,</p>
            <p>d) uchwalenie okresowych planów działania,</p>
            <p>e) składanie sprawozdań z działalności Stowarzyszenia,</p>
            <p>f) nabywanie praw i zaciąganie zobowiązań majątkowych w imieniu Stowarzyszenia, z zastrzeżeniem § 24 ust. 6.</p>
            <p>2. Zarząd zbiera się w określonych przez siebie terminach, zgodnie z potrzebami podejmowania stosownych decyzji.</p>
            <p>3. Zarząd dokonuje podziału czynności między swoich członków w sprawach niewymagających kolektywnego działania.</p>
            <p>4. Posiedzenia Zarządu zwołuje Prezes lub upoważniony członek Zarządu.</p>
            <p>5. Do obsługi administracyjnej Stowarzyszenia Zarząd może zatrudniać pracowników, którzy tworzą Biuro Stowarzyszenia.</p>
            <p>6. Członkowie Zarządu mogą otrzymywać wynagrodzenie za czynności wykonywane w związku z pełnioną funkcją.</p>
            <p>7. W umowach między Stowarzyszeniem a członkiem zarządu oraz w sporach z nim Stowarzyszenie reprezentuje członek Komisji Rewizyjnej wskazany w uchwale tego organu lub pełnomocnik powołany uchwałą Walnego Zebrania Członków.</p>

            <p class="font-bold text-center">§ 22</p>
            <p>1. Komisja Rewizyjna składa się z trzech do pięciu członków.</p>
            <p>2. Komisja Rewizyjna wybiera ze swojego grona Przewodniczącego i Wiceprzewodniczącego.</p>
            <p>3. Do kompetencji Komisji Rewizyjnej należy:</p>
            <p>a) przeprowadzenie co najmniej raz w roku kontroli merytorycznej i finansowej działalności Zarządu,</p>
            <p>b) składanie sprawozdań na Walnym Zebraniu Członków i zgłaszanie wniosków, w przedmiocie absolutorium ustępującemu Zarządowi,</p>
            <p>c) przedstawienie Zarządowi protokołów pokontrolnych wraz z wnioskami,</p>
            <p>d) prowadzenie okresowych kontroli opłacania składek członkowskich,</p>
            <p>e) składanie wniosku o zwołanie Nadzwyczajnego Walnego Zebrania Członków.</p>
            <p>4. Członkami Komisji rewizyjnej nie mogą być osoby:</p>
            <p>a) będące członkami Zarządu lub pozostające z członkiem Zarządu w stosunku pokrewieństwa, powinowactwa lub podległości z tytułu zatrudnienia,</p>
            <p>b) skazane prawomocnym wyrokiem za przestępstwo z winy umyślnej.</p>
            <p>5. Wybranie do składu Komisji osoby wbrew postanowieniom ust. 4 jest nieważne. Wystąpienie okoliczności, o których mowa w ust. 4 w trakcie kadencji Komisji jest równoznaczne z pisemną rezygnacją członka Komisji, którego okoliczność ta dotyczy.</p>

            <p class="font-bold text-center">§ 23</p>
            <p>Przewodniczący Komisji Rewizyjnej i w razie potrzeby inni jej członkowie mają prawo uczestnictwa z głosem doradczym w posiedzeniach Zarządu Stowarzyszenia.</p>

            <h3 class="text-lg font-bold text-center mt-8 pt-4" id="rozdzial-5">
                Rozdział V<br/>MAJĄTEK I DZIAŁALNOŚĆ STOWARZYSZENIA
            </h3>
            <p class="font-bold text-center">§ 24</p>
            <p>1. Majątek Stowarzyszenia tworzą środki pieniężne i inne składniki majątkowe, które służą wyłącznie do realizacji statutowych celów Stowarzyszenia.</p>
            <p>2. Majątek Stowarzyszenia pochodzi z:</p>
            <p>a) składek członkowskich,</p>
            <p>b) dotacji,</p>
            <p>c) darowizn,</p>
            <p>d) zapisów i spadków,</p>
            <p>e) dochodów z majątku,</p>
            <p>f) wpływów z działalności statutowej Stowarzyszenia,</p>
            <p>g) innych wpływów uzyskiwanych zgodnie z obowiązującymi przepisami prawa.</p>
            <p>3. Składki członkowskie powinny być wpłacane do końca pierwszego kwartału każdego roku. Nowo przyjęci członkowie wpłacają składki według zasad określonych przez Zarząd w ciągu 30 dni od otrzymania powiadomienia o przyjęciu na członka Stowarzyszenia.</p>
            <p>4. Stowarzyszenie prowadzi gospodarkę finansową oraz rachunkowość zgodnie z obowiązującymi przepisami prawa.</p>
            <p>5. Majątkiem i funduszami Stowarzyszenia gospodaruje Zarząd.</p>
            <p>6. Następujące czynności zarządu majątkiem wymagają zgody Walnego Zebrania Członków:</p>
            <p>a) nabycie, zbycie lub obciążenie nieruchomości,</p>
            <p>b) nabycie, zbycie lub objęcie udziałów lub akcji w spółce,</p>
            <p>c) zaciągnięcie kredytu lub pożyczki,</p>
            <p>d) wynajęcie lub wydzierżawienie nieruchomości Stowarzyszenia na okres dłuższy niż 3 lata,</p>
            <p>e) przyjęcie lub odrzucenie spadku.</p>

            <p class="font-bold text-center">§ 25</p>
            <p>Zabronione jest udzielanie pożyczek przez Stowarzyszenie lub zabezpieczanie zobowiązań majątkiem Stowarzyszenia w stosunku do:</p>
            <p>a) członków Stowarzyszenia i jego organów,</p>
            <p>b) pracowników Stowarzyszenia,</p>
            <p>c) małżonków pracowników oraz ich krewnych lub powinowatych w linii prostej, krewnych lub powinowatych w linii bocznej do drugiego stopnia,</p>
            <p>d) osób związanych z pracownikami z tytułu przysposobienia, opieki lub kurateli.</p>

            <p class="font-bold text-center">§ 26</p>
            <p>Oświadczenia woli w imieniu Stowarzyszenia składa dwóch członków Zarządu, w tym Prezes lub Wiceprezes.</p>

            <h3 class="text-lg font-bold text-center mt-8 pt-4" id="rozdzial-6">
                Rozdział VI<br/>PRZEPISY KOŃCOWE
            </h3>
            <p class="font-bold text-center">§ 27</p>
            <p>Statut niniejszy może być zmieniony lub zastąpiony nowym, wyłącznie przez Walne Zebranie Członków na podstawie uchwały powziętej większością dwóch trzecich głosów, przy obecności co najmniej połowy członków Stowarzyszenia; § 17 nie stosuje się.</p>

            <p class="font-bold text-center">§ 28</p>
            <p>1. Stowarzyszenie może być rozwiązane na podstawie uchwały Walnego Zebrania powziętej większością dwóch trzecich głosów, przy obecności co najmniej połowy członków Stowarzyszenia; § 17 nie stosuje się.</p>
            <p>2. Likwidatorami są członkowie Zarządu Stowarzyszenia, jeśli Walne Zebranie nie wyznaczy innych likwidatorów.</p>
            <p>3. Pozostały po likwidacji majątek Stowarzyszenia, zostanie podzielony zgodnie z uchwałą Walnego Zebrania Członków.</p>
